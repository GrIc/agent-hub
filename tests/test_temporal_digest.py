# ── render_daily ──────────────────────────────────────────────────

class TestRenderDaily:
    def test_markdown_with_commits(self, store_with_commits):
        """Daily markdown with commits contains expected sections."""
        result = render_daily(store_with_commits, date(2026, 5, 8), fmt="markdown")
        assert "# Changelog — 2026-05-08" in result
        assert "2 commits" in result
        assert "## Features" in result
        assert "## Fixes" in result
        assert "high-risk" in result
        assert "Generated by Agent Hub temporal digest" in result

    def test_markdown_empty_day(self, store_with_commits):
        """Daily markdown for a day with no commits returns empty message."""
        result = render_daily(store_with_commits, date(2026, 1, 1), fmt="markdown")
        assert "# Changelog — 2026-01-01" in result
        assert "No commits." in result

    def test_html_with_commits(self, store_with_commits):
        """Daily HTML contains expected structure."""
        result = render_daily(store_with_commits, date(2026, 5, 8), fmt="html")
        assert "<!DOCTYPE html>" in result
        assert "Changelog — 2026-05-08" in result
        assert "2 commits" in result
        assert "high-risk" in result

    def test_json_with_commits(self, store_with_commits):
        """Daily JSON is valid and contains expected fields."""
        result = render_daily(store_with_commits, date(2026, 5, 8), fmt="json")
        data = json.loads(result)
        assert data["date"] == "2026-05-08"
        assert data["total_commits"] == 2
        assert "feature" in str(data["by_intent"]).lower() or "Features" in str(data["by_intent"])
        assert len(data["commits"]) == 2

    def test_json_empty_day(self, store_with_commits):
        """Daily JSON for empty day."""
        result = render_daily(store_with_commits, date(2026, 1, 1), fmt="json")
        data = json.loads(result)
        assert data["total_commits"] == 0

    def test_slack_blocks_with_commits(self, store_with_commits):
        """Daily Slack blocks is valid JSON with blocks."""
        result = render_daily(store_with_commits, date(2026, 5, 8), fmt="slack_blocks")
        data = json.loads(result)
        assert "blocks" in data
        assert len(data["blocks"]) > 0
        # First block should be a header
        assert data["blocks"][0]["type"] == "header"

    def test_slack_empty_day(self, store_with_commits):
        """Daily Slack for empty day."""
        result = render_daily(store_with_commits, date(2026, 1, 1), fmt="slack_blocks")
        data = json.loads(result)
        assert "blocks" in data

    def test_unknown_format_raises(self, store_with_commits):
        """Unknown format raises ValueError."""
        with pytest.raises(ValueError, match="Unknown format"):
            render_daily(store_with_commits, date(2026, 5, 8), fmt="xml")

    def test_high_risk_detected(self, store_with_commits):
        """High-risk commits are flagged in markdown."""
        result = render_daily(store_with_commits, date(2026, 5, 8), fmt="markdown")
        # The SSO commit has risk_score=0.6
        assert "high-risk" in result


