# ── Edge Cases ────────────────────────────────────────────────────

class TestEdgeCases:
    def test_single_commit(self, tmp_path):
        """Digest with exactly one commit."""
        db_path = tmp_path / "commits.sqlite"
        store = TemporalStore(db_path)
        store.connection.execute("""
            INSERT INTO commits (sha, author, date, subject, body, files_json,
                intent, summary, modules_affected_json, risk_score,
                enriched_at, g_version)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            "e" * 40, "Dave", "2026-05-08T10:00:00+02:00",
            "chore: Update dependencies", "",
            json.dumps([{"path": "requirements.txt", "status": "M", "insertions": 3, "deletions": 3}]),
            "chore", "Updated dependencies.",
            json.dumps(["."]), 0.1,
            "2026-05-08T11:00:00+00:00", "1.0.0",
        ))
        result = render_daily(store, date(2026, 5, 8), fmt="markdown")
        assert "1 commit" in result

    def test_commit_with_insufficient_evidence(self, tmp_path):
        """Digest handles abstain summaries gracefully."""
        db_path = tmp_path / "commits.sqlite"
        store = TemporalStore(db_path)
        store.connection.execute("""
            INSERT INTO commits (sha, author, date, subject, body, files_json,
                intent, summary, modules_affected_json, risk_score,
                enriched_at, g_version)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """, (
            "f" * 40, "Eve", "2026-05-08T10:00:00+02:00",
            "unknown: mysterious change", "",
            json.dumps([{"path": "src/weird.py", "status": "M", "insertions": 1, "deletions": 0}]),
            "unknown", "[INSUFFICIENT_EVIDENCE]",
            json.dumps(["src"]), 0.2,
            "2026-05-08T11:00:00+00:00", "1.0.0",
        ))
        result = render_daily(store, date(2026, 5, 8), fmt="markdown")
        assert "Changelog" in result
        assert "mysterious" in result
