class TestSlackChannel:
    def test_missing_env_var(self):
        """Missing env var logs error and returns."""
        # Ensure the env var is NOT set
        env_key = "NONEXISTENT_WEBHOOK_URL"
        if env_key in os.environ:
            del os.environ[env_key]

        channel = SlackChannel(webhook_url_env=env_key)
        channel.send("{}", meta={})  # Should not crash

    @mock.patch("urllib.request.urlopen")
    @mock.patch("urllib.request.Request")
    def test_send_slack_blocks(self, mock_request, mock_urlopen, tmp_path):
        """SlackChannel sends slack_blocks format."""
        # Set the env var
        os.environ["TEST_WEBHOOK"] = "https://hooks.slack.com/test"
        try:
            channel = SlackChannel(webhook_url_env="TEST_WEBHOOK", channel="#test")
            payload = json.dumps({"blocks": [{"type": "header", "text": {"type": "plain_text", "text": "Test"}}]})

            mock_resp = mock.MagicMock()
            mock_resp.status = 200
            mock_urlopen.return_value = mock_resp
            mock_resp.__enter__ = mock.MagicMock(return_value=mock_resp)
            mock_resp.__exit__ = mock.MagicMock(return_value=False)

            channel.send(payload, fmt="slack_blocks", meta={})

            mock_request.assert_called_once()
            call_args = mock_request.call_args
            assert call_args[0][0] == "https://hooks.slack.com/test"
        finally:
            os.environ.pop("TEST_WEBHOOK", None)

    def test_dry_run_prints_payload(self):
        """Dry-run mode would print the JSON payload (tested via mock)."""
        os.environ["TEST_WEBHOOK"] = "https://hooks.slack.com/test"
