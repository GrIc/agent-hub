class TestLoadChannels:
    def test_load_file_channel(self):
        """File channel loads from config."""
        config = {
            "temporal": {
                "delivery": [
                    {"type": "file", "path": "context/changelog/{date}.md"},
                ]
            }
        }
        channels = load_channels(config)
        assert len(channels) == 1
        assert isinstance(channels[0], FileChannel)

