# ── FileChannel ───────────────────────────────────────────────────

class TestFileChannel:
    def test_write_to_file(self, tmp_path):
        """FileChannel writes content to the specified path."""
        channel = FileChannel(path=str(tmp_path / "changelog" / "{date}.md"))
        meta = {"date": "2026-05-08"}
        channel.send(SAMPLE_CONTENT, meta=meta)

        expected = tmp_path / "changelog" / "2026-05-08.md"
        assert expected.exists()
        assert expected.read_text(encoding="utf-8") == SAMPLE_CONTENT

    def test_template_with_year_month_day(self, tmp_path):
        """Template variables are resolved correctly."""
        channel = FileChannel(path=str(tmp_path / "{year}" / "{month}" / "{day}.md"))
        meta = {"date": "2026-05-08", "year": "2026", "month": "05", "day": "08"}
        channel.send(SAMPLE_CONTENT, meta=meta)

        expected = tmp_path / "2026" / "05" / "08.md"
        assert expected.exists()

    def test_creates_parent_directories(self, tmp_path):
        """Parent directories are created if they don't exist."""
        channel = FileChannel(path=str(tmp_path / "a" / "b" / "c" / "output.md"))
        channel.send(SAMPLE_CONTENT, meta={})
        assert (tmp_path / "a" / "b" / "c" / "output.md").exists()


