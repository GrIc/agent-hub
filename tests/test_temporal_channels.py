# ── EmailChannel ──────────────────────────────────────────────────

class TestEmailChannel:
    def test_no_recipients(self):
        """EmailChannel with no recipients logs warning."""
        channel = EmailChannel(smtp_host_env="SMTP_HOST", to=[])
        # Should not send, but should not crash
        channel.send(SAMPLE_CONTENT, meta={"date": "2026-05-08"})

    def test_smtp_connection_failed(self):
        """EmailChannel handles SMTP failure gracefully."""
        channel = EmailChannel(
            smtp_host_env="SMTP_HOST",
            smtp_port_env="SMTP_PORT",
            from_addr="test@example.com",
            to=["dev@example.com"],
        )
        # Set env vars
        os.environ["SMTP_HOST"] = "localhost"
        os.environ["SMTP_PORT"] = "25"
        try:
            # This will fail to connect, but should not crash
            channel.send(SAMPLE_CONTENT, meta={"date": "2026-05-08"})
        except Exception:
            pass  # Expected to fail on localhost
        finally:
            os.environ.pop("SMTP_HOST", None)
            os.environ.pop("SMTP_PORT", None)

    @mock.patch("smtplib.SMTP")
    def test_send_success(self, mock_smtp):
        """EmailChannel sends successfully."""
        mock_server = mock.MagicMock()
        mock_smtp.return_value = mock_server

        channel = EmailChannel(
            smtp_host_env="SMTP_HOST",
            smtp_port_env="SMTP_PORT",
            from_addr="test@example.com",
            to=["dev@example.com"],
        )
        os.environ["SMTP_HOST"] = "smtp.example.com"
        os.environ["SMTP_PORT"] = "587"
        try:
            channel.send(SAMPLE_CONTENT, meta={"date": "2026-05-08"})
            mock_server.sendmail.assert_called_once()
        finally:
            os.environ.pop("SMTP_HOST", None)
            os.environ.pop("SMTP_PORT", None)
